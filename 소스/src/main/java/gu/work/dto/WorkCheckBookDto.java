package gu.work.dto;

public class WorkCheckBookDto {
	private String empNo;
	private String onTime;
	private String offTime;
	private String onCheck;
	private String offCheck;
	private String checkFlag;
	
	
	
	public String getEmpNo() {
		return empNo;
	}
	public void setEmpNo(String empNo) {
		this.empNo = empNo;
	}
	public String getOnTime() {
		return onTime;
	}
	public void setOnTime(String onTime) {
		this.onTime = onTime;
	}
	public String getOffTime() {
		return offTime;
	}
	public void setOffTime(String offTime) {
		this.offTime = offTime;
	}
	public String getOnCheck() {
		return onCheck;
	}
	public void setOnCheck(String onCheck) {
		this.onCheck = onCheck;
	}
	public String getOffCheck() {
		return offCheck;
	}
	public void setOffCheck(String offCheck) {
		this.offCheck = offCheck;
	}
	public String getCheckFlag() {
		return checkFlag;
	}
	public void setCheckFlag(String checkFlag) {
		this.checkFlag = checkFlag;
	}
	
	
}
